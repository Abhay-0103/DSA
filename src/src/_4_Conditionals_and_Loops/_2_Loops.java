package _4_Conditionals_and_Loops;

public class _2_Loops {
    public static void main(String[] args) {
        // Q: Print number from 1 to 5

        /*
                syntax of For Loop
                for (int ; condion; +/-) {
                     // body
                }
         */

        for (int num = 1; num <=5; num++ ){
            System.out.println(num);
        }



    }
}
